#include "functions.h"

/**
 * add - Adds two numbers.
 * @a: First number.
 * @b: Second number.
 * Return: The sum of the two numbers.
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Subtracts one number from another.
 * @a: First number.
 * @b: Second number.
 * Return: The difference between the two numbers.
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Multiplies two integers.
 * @a: First number.
 * @b: Second number.
 * Return: The multiplied value of the two numbers.
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Divides a number by another
 * @a: First number.
 * @b: Second number.
 * Return: The value of the division.
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Calculates the modulus value of two numbers
 * @a: First number.
 * @b: Second number.
 * Return: The value of the modulus operation.
 */

int mod(int a, int b)
{
	return (a % b);
}
